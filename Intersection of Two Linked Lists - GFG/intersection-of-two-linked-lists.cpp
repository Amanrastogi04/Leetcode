class Solution{
  public:
    Node* findIntersection(Node* head1, Node* head2)
    {
        unordered_set<int> st;
        Node* curr=head2;
        Node* temp=head1;
        Node* dummy=new Node(INT_MIN);
        Node* f=dummy;
        
        while(curr!=NULL){
            st.insert(curr->data);
            curr=curr->next;
        }
        
        while(temp!=NULL){
            if(st.find(temp->data)!=st.end()){
                f->next=new Node(temp->data);
                f=f->next;
            }
            temp=temp->next;
        }
        
        return dummy->next;
        
    }
};

